package com.chinmay.it.mfg.bean;

public class PlantStationDetailsBean {

	private String id;
	private String plantCode;
	private String stationNumber;
	private String stationName;
	private String status;
	private String stationVersion;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPlantCode() {
		return plantCode;
	}

	public void setPlantCode(String plantCode) {
		this.plantCode = plantCode;
	}

	public String getStationNumber() {
		return stationNumber;
	}

	public void setStationNumber(String stationNumber) {
		this.stationNumber = stationNumber;
	}

	public String getStationName() {
		return stationName;
	}

	public void setStationName(String stationName) {
		this.stationName = stationName;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getStationVersion() {
		return stationVersion;
	}

	public void setStationVersion(String stationVersion) {
		this.stationVersion = stationVersion;
	}
}
